public class comparision{
    static public void main(String[] args){

        // 1. Greater than
        System.err.println(10 > 20); // false
        System.err.println(20 > 10); // true

        // 2. less than 
        System.err.println(100 < 200); // true
        System.err.println(200 < 100); // false


        // 3. greater than and equal to
        System.err.println(50 >= 51); // false
        System.err.println(50 >= 50); // true
        System.err.println(50 >= 49); // true


        // 4. less than and equal to
        System.err.println(20 <= 21); // true
        System.err.println(20 <= 20); // true
        System.err.println(20 <= 19); // false

        


    }
}